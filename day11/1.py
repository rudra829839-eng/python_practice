# Assignment operator :-


a  = 10
b = 23

print( a + b)


a  = 10

b = 23

a = a + b     # OR a = 10 + 23  => a = 33 

print("the value of a is : ", a)

a = 10

b = 23

a += b     # OR a = a + b  => a = 10 + 23  => a = 33

print("the value of a is : ", a)

a = 33

b = 23

a = a - b     # OR a = a - b  => a = 33 - 23  => a = 10

print("the value of a is : ", a)

a  = 33

b = 23

a -= b     # OR a = a - b  => a = 33 - 23  => a = 10

print("the value of a is : ", a)

